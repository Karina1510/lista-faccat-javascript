let numero = parseInt(prompt("Digite um número DE 1 A 7 PARA SABER O DIA DA SEMANA: "))

switch(numero){
    case 1:
        alert("Domingo")
        break
    case 2:
        alert("Segunda")
        break
    case 3:
        alert("terça")
        break
    case 4:
        alert("quarta")
        break
    case 5:
        alert("quinta")
        break
    case 6:
        alert("sexta")
        break
    case 7:
        alert("Sábado")
        break
    default:
        alert("Número incorreto!")
        break
}
